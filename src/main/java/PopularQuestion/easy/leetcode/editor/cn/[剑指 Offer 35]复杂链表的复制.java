class ComplexLinkedListCopy {
    class Node {
        int val;
        Node next;
        Node random;

        public Node(int val) {
            this.val = val;
            this.next = null;
            this.random = null;
        }
    }
    public Node copyRandomList(Node head) {
        Node copyNode = new Node(0);
        Node res = copyNode;
        Node randomNode = copyNode;
        Node pre = null;
        Node temp = head;
        while(temp!=null){
            copyNode.val = temp.val;
            pre = copyNode;
            if(temp.next!=null) {
                copyNode = new Node(0);
            }else{
                copyNode = null;
            }
            pre.next = copyNode;
            temp = temp.next;
        }

        temp = head;
        while(temp!=null){
            Node r = res;
            Node t = temp.random;
            Node temp1 = head;
            int len = 0;
            while(temp1!=null){
                if(t==temp1)
                    break;
                len++;
                temp1 = temp1.next;
            }
            while(len>0){
                r = r.next;
                --len;
            }
            randomNode.random = r;
            randomNode = randomNode.next;
            temp = temp.next;
        }
        return res;
    }

    //èŠ‚ç‚¹æ˜ å°„
    public Node copyRandomList1(Node head){
        Node temp = head;
        Map<Node, Node> map = new HashMap<>();
        while(temp!=null){
            map.put(temp,new Node(temp.val));
            temp = temp.next;
        }
        temp = head;
        while(temp!=null){
            map.get(temp).next = map.get(temp.next);
            map.get(temp).random = map.get(temp.random);
            temp = temp.next;
        }
        return map.get(head);
    }
}
